package com.business.inventra.dto;

import java.util.List;

public class CustomerDTO {
    private String id;
    private String firstName;
    private String middleName;
    private String lastName;
    private String email;
    private String orgID;
    private String branchId;
    private List<AddressDTO> addresses;
    private List<ContactDTO> contacts;

    public CustomerDTO() {
    }

    public CustomerDTO(String id, String firstName, String middleName, String lastName, String email, String orgID,
                      List<AddressDTO> addresses, List<ContactDTO> contacts, String branchId) {
        this.id = id;
        this.firstName = firstName;
        this.middleName = middleName;
        this.lastName = lastName;
        this.email = email;
        this.orgID = orgID;
        this.addresses = addresses;
        this.contacts = contacts;
        this.branchId = branchId;
    }

    // Getters and Setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getMiddleName() {
        return middleName;
    }

    public void setMiddleName(String middleName) {
        this.middleName = middleName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getOrgID() {
        return orgID;
    }

    public void setOrgID(String orgID) {
        this.orgID = orgID;
    }

    public String getBranchId() {
        return branchId;
    }

    public void setBranchId(String branchId) {
        this.branchId = branchId;
    }

    public List<AddressDTO> getAddresses() {
        return addresses;
    }

    public void setAddresses(List<AddressDTO> addresses) {
        this.addresses = addresses;
    }

    public List<ContactDTO> getContacts() {
        return contacts;
    }

    public void setContacts(List<ContactDTO> contacts) {
        this.contacts = contacts;
    }
} 